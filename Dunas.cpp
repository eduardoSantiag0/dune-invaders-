Dunas::Dunas (int m_pos_x, int m_pos_y) : m_pos_x(m_pos_x), m_pos_y(m_pos_y)
{   
    for (unsigned int row = 0; row < grid.size(); ++row) {
        for (unsigned int column = 0; column < grid[0].size(); ++column) {
            if (grid[row][column] == 1) {
                
                // Posição baseada na número da coluna
                float pos_x = m_pos_x + column * BLOCK_WIDTH;
                float pos_y = m_pos_y + row * BLOCK_HEIGHT;
                Block block = Block(pos_x, pos_y);
                blocks.push_back(block);
            }
        }
    }
}

void Dunas::draw (SDL_Renderer* render) {
    for (auto& block:blocks) {
        block.draw(render);
    }
}

